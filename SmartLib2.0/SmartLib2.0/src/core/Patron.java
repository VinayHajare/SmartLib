package core;

/**
 *
 * @author Hydron
 */
class Patron {
    
    private final String id;
    private final String email;

    public Patron(String id, String email) {
        this.id = id;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }
    
}
