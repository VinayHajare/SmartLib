    private boolean isBorrowerInputValid() {
        if (bw_id.getText().isEmpty() || bw_name.getText().isEmpty() || bw_address.getText().isEmpty() || bw_email.getText().isEmpty() || bw_contact.getText().isEmpty() || bw_borrowed.getText().isEmpty() || bw_amount.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all the details !!", "Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        try {
            if (Integer.parseInt(bw_id.getText()) < 1) {
                JOptionPane.showMessageDialog(this, "Invalid Borrower Id", "Error", JOptionPane.WARNING_MESSAGE);
                return false;
            }
            Double.valueOf(bw_amount.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid number format for ID or amount", "Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return !(!InputValidator.validateEmail(bw_email.getText(), rootPane) || !InputValidator.validateMobileNumber(bw_contact.getText(), rootPane));
    }

