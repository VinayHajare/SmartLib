    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Add borrower action
    if (isBorrowerInputValid()) {
            Connection con = null;
            PreparedStatement ps = null;

            // Storing user data entered into the string
            String id = bw_id.getText();
            String name = bw_name.getText();
            String address = bw_address.getText();
            String email = bw_email.getText();
            String contact = bw_contact.getText();
            String borrowed = bw_borrowed.getText();
            String amount = bw_amount.getText();

            // SQL Query
            final String SQL = "INSERT INTO borrowers(BOR_ID, BOR_NAME, BOR_ADDRESS, BOR_EMAIL, BOR_CONTACT, BK_BORROWED, FINE_AMT) VALUES (?, ?, ?, ?, ?, ?, ?)";

            // Performing the Database operation
            try {
                con = Connect.dbconnection();
                ps = con.prepareStatement(SQL);

                ps.setString(1, id);
                ps.setString(2, name);
                ps.setString(3, address);
                ps.setString(4, email);
                ps.setString(5, contact);
                ps.setString(6, borrowed);
                ps.setString(7, amount);
                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Borrower Added successfully !!", "Success", JOptionPane.INFORMATION_MESSAGE);

                clearBorrowerFields();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Database Operation Failed !!!", "Database ERROR", JOptionPane.ERROR_MESSAGE);
                LoggerUtil.logError("Database operation failed", ex);

            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Database driver not found!!!", "Database Driver ERROR", JOptionPane.ERROR_MESSAGE);
                LoggerUtil.logError("Database driver not found", ex);

            }catch (HeadlessException ex) {
              LoggerUtil.logError("Display system no available", ex);
            }finally {
                try {
                    if (ps != null) {
                        ps.close();
                    }
                    if (con != null) {
                        con.close();
                    }
                } catch (SQLException e) {
                    LoggerUtil.logError("Failed to close database resources", e);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

